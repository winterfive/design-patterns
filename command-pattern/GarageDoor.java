package controllables;

public class GarageDoor {
	
	public void open() {
		System.out.println("The garage door is opening.");
	}
	
	public void close() {
		System.out.println("The garage door is closing.");
	}
	
	public void stop() {
		System.out.println("The garage door has stopped.");
	}
	
	public void lightOn() {
		System.out.println("The garage light is on.");
	}
	
	public void lightOff() {
		System.out.println("The garage light is off.");
	}

}
